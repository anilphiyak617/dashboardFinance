import { createApi, fetchBaseQuery } from "@reduxjs/toolkit/query/react";
import { GetKpisResponse, GetProductsResponse, GetTransactionResponse } from "./types";

export const api = createApi({
  reducerPath: "main",
  // used for invalidation and caechcing
  tagTypes: ["Kpis", "Products", "Transactions"],
  baseQuery: fetchBaseQuery({ baseUrl: import.meta.env.VITE_BASE_URL }),
  endpoints: (builder) => ({
    getKpis: builder.query<Array<GetKpisResponse>, void>({
      query: () => `/kpi/kpis`,
      providesTags: ["Kpis"],
    }),

    getProducts: builder.query<Array<GetProductsResponse>, void>({
      query: () => "/product/products",
      providesTags: ["Products"],
    }),

    getTransactions: builder.query<Array<GetTransactionResponse>, void>({
      query: () => "/transaction/transactions",
      providesTags: ["Transactions"],
    }), 
  }),
});

/* exporting autogenerated hooks for using in the components */
/* GET /product/products  */
export const { useGetKpisQuery, useGetProductsQuery, useGetTransactionsQuery } =
  api;
